WITH cte AS(
SELECT
request_at, 
1.0 * (COUNT(CASE WHEN status IN ('cancelled_by_client','cancelled_by_driver') THEN 1 ELSE NULL END)) AS canceled_trips,
1.0*(COUNT (0)) AS total_trips
FROM trips t
INNER JOIN  users u 
ON t.client_id = u.users_id
where u.banned = 'No'
GROUP BY request_at
)

select request_at,canceled_trips,total_trips,ROUND(((canceled_trips/total_trips)*100),2) AS cancellation_rate
FROM cte

